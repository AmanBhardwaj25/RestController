package com.example.servingwebcontent;

public class Greeting {
	
	private int id;
	private String name;

	public void setID(int i)
	{
		id = i;
	}
	
	public void setName(String name)
	{
		this.name = name;
	}
	
	public String getName()
	{
		return name;
	}
	
	public long getID()
	{
		return id;
	}
	

}
